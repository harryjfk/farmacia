package pe.gob.minsa.farmacia.domain;

public class Ici {
    
    private int IdICI; 
    private Integer IdTipoSuministro; 
    private int IdPeriodo;

    public int getIdICI() {
        return IdICI;
    }

    public void setIdICI(int IdICI) {
        this.IdICI = IdICI;
    }

    public Integer getIdTipoSuministro() {
        return IdTipoSuministro;
    }

    public void setIdTipoSuministro(Integer IdTipoSuministro) {
        this.IdTipoSuministro = IdTipoSuministro;
    }

    public int getIdPeriodo() {
        return IdPeriodo;
    }

    public void setIdPeriodo(int IdPeriodo) {
        this.IdPeriodo = IdPeriodo;
    }  
}
